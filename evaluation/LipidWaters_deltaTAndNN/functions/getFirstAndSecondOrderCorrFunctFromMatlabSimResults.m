function [avgCorrFuncFirstOrder_theta_phi,avgCorrFuncSecondOrder_theta_phi] ...
    = getFirstAndSecondOrderCorrFunctFromMatlabSimResults( ...
    simulationResults,whichNNCase)

avgCorrFuncFirstOrder_theta_phi = ...
    simulationResults.sumCorrelationFunctionSaverFirstOrder.( ...
    whichNNCase)/simulationResults.atomCounter;
avgCorrFuncSecondOrder_theta_phi = ...
    simulationResults.sumCorrelationFunctionSaverSecondOrder.( ...
    whichNNCase)/simulationResults.atomCounter;

end