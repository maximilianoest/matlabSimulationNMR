function [avgCorrFuncFirstOrder_theta_phi,avgCorrFuncSecondOrder_theta_phi] ...
    = loadFirstAndSecondOrderCorrelationFunctionsFromNNSimulation( ...
    filePath,whichNNCase)

results = load(filePath);
avgCorrFuncFirstOrder_theta_phi = ...
    results.sumCorrelationFunctionSaverFirstOrder.(whichNNCase) ...
    /results.atomCounter;
avgCorrFuncSecondOrder_theta_phi = ...
    results.sumCorrelationFunctionSaverSecondOrder.(whichNNCase) ...
    /results.atomCounter;


end