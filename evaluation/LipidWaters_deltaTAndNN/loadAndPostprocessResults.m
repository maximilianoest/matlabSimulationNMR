function relevantResults = loadAndPostprocessResults(filePath ...
    ,lags,offsetReductionRegion)

if strcmp(filePath,"")
    error("Matfile not found. ");
end

results = load(filePath);
for nearestNeighboursCase = fieldnames( ...
        results.sumCorrelationFunctionSaverFirstOrder)'
    avgCorrFuncsZerothOrder_theta_phi = results ...
        .sumCorrelationFunctionsSaverZerothOrder.( ...
        nearestNeighboursCase{1})/results.atomCounter;
    avgCorrFuncFirstOrder_theta_phi = results ...
        .sumCorrelationFunctionSaverFirstOrder.( ...
        nearestNeighboursCase{1})/results.atomCounter;
    avgCorrFuncSecondOrder_theta_phi = results ...
        .sumCorrelationFunctionSaverSecondOrder.( ...
        nearestNeighboursCase{1})/results.atomCounter;
    
    for thetaNr = 1:size(avgCorrFuncFirstOrder_theta_phi,1)
        for phiNr = 1:size(avgCorrFuncFirstOrder_theta_phi,2)

            shortenedCorrFuncZerothOrder = squeeze(avgCorrFuncsZerothOrder_theta_phi(thetaNr,phiNr,1:lags))';
            shortenedCorrFuncFirstOrder = squeeze(avgCorrFuncFirstOrder_theta_phi(thetaNr,phiNr,1:lags))';
            shortenedCorrFuncSecondOrder = squeeze(avgCorrFuncSecondOrder_theta_phi(thetaNr,phiNr,1:lags))';
            
            offsetReducedCorrFuncZerothOrder_theta_phi(thetaNr,phiNr,:) = shortenedCorrFuncZerothOrder - mean(shortenedCorrFuncZerothOrder(round(offsetReductionRegion(1)*end):round(offsetReductionRegion(2)*end))); %#ok<AGROW>
            offsetReducedCorrFuncFirstOrder_theta_phi(thetaNr,phiNr,:) = shortenedCorrFuncFirstOrder - mean(shortenedCorrFuncFirstOrder(round(offsetReductionRegion(1)*end):round(offsetReductionRegion(2)*end))); %#ok<AGROW>
            offsetReducedCorrFuncSecondOrder_theta_phi(thetaNr,phiNr,:) = shortenedCorrFuncSecondOrder - mean(shortenedCorrFuncSecondOrder(round(offsetReductionRegion(1)*end):round(offsetReductionRegion(2)*end))); %#ok<AGROW>
        end
    end
    postProcessedCorrFuncZerothOrder_theta_phi.(nearestNeighboursCase{1}) = offsetReducedCorrFuncZerothOrder_theta_phi;
    postProcessedCorrFuncFirstOrder_theta_phi.(nearestNeighboursCase{1}) = offsetReducedCorrFuncFirstOrder_theta_phi;
    postProcessedCorrFuncSecondOrder_theta_phi.(nearestNeighboursCase{1}) = offsetReducedCorrFuncSecondOrder_theta_phi;
end

relevantResults = struct( ...
    'deltaT', results.deltaTInS ...
    ,'simulationDuration', results.simulationDurationInS ...
    ,'corrFuncZerothOrder', postProcessedCorrFuncZerothOrder_theta_phi...
    ,'corrFuncFirstOrder' , postProcessedCorrFuncFirstOrder_theta_phi ...
    ,'corrFuncSecondOrder', postProcessedCorrFuncSecondOrder_theta_phi ...
    ,'fibreAnglesTheta', rad2deg(results.fibreAnglesTheta) ...
    ,'fibreAnglesPhi', rad2deg(results.fibreAnglesPhi) ...
    ,'nearestNeighbourCases', results.nearestNeighbourCases ...
    ,'gromacsFileName', results.fileName);

end