function createDataSavingObject()
variablesInformation = evalin('base','whos');

variablesNotToSave = ["atomNumber","atomTimerStart","averageTimeForOneAtom" ...
    ,"azimuthAngle","calculationSteps","correlationFunctionFirstOrder" ...
    ,"correlationFunctionSecondOrder","correlationFunctionZerothOrder" ...
    ,"correlationFunctionFirstOrderSingle" ...
    ,"correlationFunctionSecondOrderSingle" ...
    ,"correlationFunctionZerothOrderSingle"...
    ,"correlationFunctionFirstOrderShortPadding" ...
    ,"correlationFunctionSecondOrderShortPadding" ...
    ,"correlationFunctionZerothOrderShortPadding" ...
    ,"dataSavingObject","fibreAnglesPhiCount","fibreAnglesThetaCount" ...
    ,"nearestNeighbourDistancesPow3","nearestNeighboursIDs","phi" ...
    ,"phiNumber","polarAngle","positionTimer","rotatedX","rotatedY" ...
    ,"rotatedZ","spectralDensityFirstOrder","spectralDensitySecondOrder" ...
    ,"sphericalHarmonicFirstOrder","sphericalHarmonicSecondOrder" ...
    ,"sphericalHarmonicZerothOrder","theta","thetaNumber","trajectoryX" ...
    ,"trajectoryY","trajectoryZ","transformationTimer","yAxis","zAxis" ...
    ,"reducedCorrelationFunction0W0","reducedCorrelationFunction1W0" ...
    ,"reducedCorrelationFunction2W0","yAxis","zAxis"];

variablesCount = size(variablesInformation,1);

for variableNr = 1:variablesCount
    variable = variablesInformation(variableNr);
    if ~sum(variable.name == variablesNotToSave)
        evalin('base',['dataSavingObject.' variable.name ...
            '= ' variable.name ';'])
    end
    
end

end
