function checkForEqualConfigurationsInNewAndOldSimulation( ...
    newSimulationConfigParameter,oldSimulationConfigParameter)
if length(newSimulationConfigParameter)  ...
        ~= length(oldSimulationConfigParameter) || ~all( ...
        newSimulationConfigParameter == oldSimulationConfigParameter)
    error("%s configuration parameter does not match with the" ...
         + " old configuration",inputname(1));
end

end