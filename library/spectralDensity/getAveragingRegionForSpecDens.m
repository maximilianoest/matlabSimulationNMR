function averagingRegionForSpecDens = getAveragingRegionForSpecDens( ...
    averagingRegionFromConfig,logFilePath)

averagingRegionForSpecDens = getValuesFromStringEnumeration( ...
    averagingRegionFromConfig,';','numeric');

logMessage(sprintf(['Spectral density will be averaged over the part' ...
    ' %.2f to %.2f of the full length of the correlation function'] ...
    ,averagingRegionForSpecDens(1),averagingRegionForSpecDens(2)) ...
    ,logFilePath,false);


end