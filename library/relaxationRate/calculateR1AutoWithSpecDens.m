function r1Auto = calculateR1AutoWithSpecDens(specDensZerothOrder ...
    ,specDensFirstOrder,specDensSecondOrder,dipoleDipoleConstant)

r1Auto = dipoleDipoleConstant*( ...
    1/12*abs(real(mean(specDensZerothOrder))) ...
    + 3/2*abs(real(mean(specDensFirstOrder))) ...
    + 3/4*abs(real(mean(specDensSecondOrder))));

end